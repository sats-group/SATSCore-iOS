private extension NeoSATSColorSwiftUI {
    static func satsColor(for colorName: SATSColorName) -> Color {
        guard let definition = SATSColor.definitions[colorName] else {
            fatalError("❌ no color definition for \(colorName) found")
        }

        return definition.color
    }
}

extension Color {
    static let satsOne = NeoSATSColorSwiftUI()
