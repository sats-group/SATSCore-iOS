class ColorsTests: XCTestCase {
    /// To ensure we expose the named colors as easy to access class variables in Color
    /// Then a color like `SATSColorName.backgroundPrimary` is available as `Color.backgroundPrimary`
    func testAllNamedColorsAreExposedAsClassVariables_forSwiftUI() {
        XCTAssertEqual(swiftUIColorConstants().count, SATSColorName.allCases.count)
    }

    /// To ensure we expose the named colors as easy to access class variables in UIColor
    /// Then a color like `SATSColorName.backgroundPrimary` is available as `UIColor.backgroundPrimary`
    func testAllNamedColorsAreExposedAsClassVariables_forUIKit() {
        XCTAssertEqual(uiKitColorConstants().count, SATSColorName.allCases.count)
    }

    func testColorPrimitives() {
        // if a primitive cannot be converted to a UIColor value
        // there will be a fatal error
        ColorPrimitive.allCases.forEach { primitive in
            _ = primitive.uiColor
        }
    }

